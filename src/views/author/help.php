<h4>
    Список авторов статей
</h4>
<p>
    Авторы статей выбираются при добавлении и редактировании статьи.
</p>
<p>
    У статьи может быть несколько авторов или, если это не важно,  не быть ни одного.
</p>
<p>
    Пользователь может быть автором, но не обязательно. 
    Скорее пользователь, это человек, который набирает и корректирует статьи.
</p>
