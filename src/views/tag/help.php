<p>
    Метка - это одно или несколько слов, разделенных пробелом.
    Статья может быть помечена несколькими метками.
    Перечислять метки надо через запятую.
</p>
<p>
    Пользуясь данным экраном вы можете заменять и удалять метки.
    В этом случае выбранная метка будет изменена или удалена во всех статьях, где она присутствует.
</p>
