<h4>Дерево рубрик</h4>
<p>
    Вы можете добавлять новые рубрики, заменять названия существующих рубрик и удалять рубрики.
</p>
<p>
    Новая рубрика может иметь родительскую рубрику.
</p>
<p>
    Позиция - это целое число, которое определяет положение рубрики в списке.
</p>
<p>
    При удалении рубрики, все статьи, относившиеся к ней, переходят в корневую рубрику.
</p>
