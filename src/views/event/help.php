<h4>Календарь</h4>
<p>
    О предстоящем событии можно информировать посетителей сайта в статье.
    Статья должна быть закреплена за рубрикой со слагом <code>events</code>.
    Если такой рубрики еще нет, необходимо ее создать.
</p>
<p>
    Опубликовав статью можно добавить событие, указав сроки, ответственного и связав
    событие с добавленной статьей.
</p>
<p>
    Записи о событиях используются в виджетах, календаре во <code>frontend</code>.
</p>
